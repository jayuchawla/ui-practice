// 1. if x>y 3 pts, x=y 1 pt, x<y 0 pts
const calcPts = (arr) => {
    let s = 0
    arr.forEach((match) => {
        const[x,y] = match.split(":")
        s += x>y ? 3:x===y ? 1:0;
    })
    return s
}
const calcPtsReduce = (arr) => {
    return arr.reduce((accum, item) => {
        const[x,y] = item.split(":")
        accum+=x>y ? 3:x===y ? 1:0
        return accum
    }, 0)
}
