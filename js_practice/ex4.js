// 3. people currently older than 30
const peopleOlderThan30 = (arr) => {
    return arr.filter((person) => {
        if(!person["dob"]) {return false}
        dob = new Date(person["dob"])
        now = new Date()
        let age = now.getFullYear() - dob.getFullYear()
        if(now.getMonth() < dob.getMonth()) {age-=1}
        if((now.getMonth() === dob.getMonth()) && (now.getDate() < dob.getDate())) {age-=1}

        if(age>=30) {return true}
        else {return false;}
    })
}
